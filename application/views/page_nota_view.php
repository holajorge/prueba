				<h1 class="title-nota">Fuente del pescador</h1>
				<p class="nota">Fecha de publicación: 27/02/2017 por Lorem ipsum dolor sitamet</p>
				<div class="divider"></div>

				<h5 >Descripción</h5>
				<p class="justify-text">Sin lugar a duda, la Fuente del Pescador es un lugar obligado para todo visitante, pues tras su reciente remodelación se ha convertido en un parador fotográfico.Ubicado sobre el boulevard Bahía cerca de la Explanada de la Bandera, este sitio tiene un encanto peculiar, pues además que su infraestructura turística llama la atención, genera un sentimiento de pertenencia a los habitantes capitalinos pues tiene la leyenda “Chetumal. Aquí inicia México”.<br><br>Rodeado por una fuente, un corredor de madera que se adentra a la bahía y frente a él un gran reloj artesanal, El Pescador, que con su atarraya en mano está listo para atrapar la chihua se yergue sobre un montículo de piedras. Este monumento, creación de la escultora Rosa María Ponzanelli, ha logrado convertirse en uno de los íconos más representativos de la capital.<br><br>Es necesario mencionar que, el título “Chetumal, aquí inicia México” se hizo famoso en el año 2009 cuando el reconocido escritor Carlos Fuentes, recibió el Doctorado Honoris Causa por parte de la Universidad de Quintana Roo y en su discurso expresó “México no termina en Chetumal, sino que empieza en Chetumal. Aquí comienza Quintana Roo. En esta frontera sur nuestra Latinoamérica se encuentra con nuestro país”.</p>
